const testing = std.testing;

test "textValue: numeric coercion mirrors binary protocol typing" {
    try testing.expectEqual(@as(i64, -42), textValue(proto.MYSQL_TYPE_LONG, "-42").int);
    try testing.expectEqual(@as(i64, 9001), textValue(proto.MYSQL_TYPE_LONGLONG, "9001").int);
    try testing.expectApproxEqAbs(@as(f64, 3.5), textValue(proto.MYSQL_TYPE_DOUBLE, "3.5").real, 1e-9);
    // A VARCHAR stays a string slice.
    try testing.expectEqualStrings("hi", textValue(proto.MYSQL_TYPE_VAR_STRING, "hi").str);
    // A malformed numeric falls back to the raw string rather than crashing.
    try testing.expectEqualStrings("notnum", textValue(proto.MYSQL_TYPE_LONG, "notnum").str);
}

test "dialBlocking: unresolvable host fails cleanly (no hang, no crash)" {
    // A syntactically-valid but unroutable .invalid TLD must surface
    // DnsFailed/ConnectFailed quickly, never block the suite.
    const r = dialBlocking("nonexistent.invalid", 3306, 500);
    try testing.expectError(error.DnsFailed, r);
}
